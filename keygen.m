function subkeys = keygen( key )

subkeys = cell(1,16);

PC1 = [...
    57 49 41 33 25 17 9 ...
    1 58 50 42 34 26 18 ...
    10 2 59 51 43 35 27 ...
    19 11 3 60 52 44 36;
    63 55 47 39 31 23 15 ...
    7 62 54 46 38 30 22 ...
    14 6 61 53 45 37 29 ...
    21 13 5 28 20 12 4];
PC2 = [...
    14 17 11 24 1 5 3 28 ...
    15 6 21 10 23 19 12 4 ...
    26 8 16 7 27 20 13 2 ...
    41 52 31 37 47 55 30 40 ...
    51 45 33 48 44 49 39 56 ...
    34 53 46 42 50 36 29 32];
shiftbits = [1 1 2 2 2 2 2 2 1 2 2 2 2 2 2 1];
% original 64 bit input, goes through and transpose the valid 56 bits
C = key(PC1(1,:))';
D = key(PC1(2,:))';
%divided into two parts
%each part is cyclically shifted in each cycle 16 times
for round = 1:16
    C = [C(1+shiftbits(round):end), C(1:shiftbits(round))];
    D = [D(1+shiftbits(round):end), D(1:shiftbits(round))];
    raw_subkey = [C,D];
    subkeys{round} = raw_subkey(PC2)';
	%A total of 16 cycles are performed to generate 16 48bits word keys
end

end
